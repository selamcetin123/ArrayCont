package Day14_DoWhileAndNestedLoop;

public class C2_Nestedstatement {

    public static void main(String[] args) {

        int week= 3;
        int days= 6;


        for (int i = 0; i <=week  ; i++) {

            System.out.println(" week :" +week);

            for (int j = 0; j <= days ; j++) {
                System.out.println( "days:" +days);

            }

        }
    }
}
