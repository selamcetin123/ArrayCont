package Day14_DoWhileAndNestedLoop;

public class C3_Example {

    public static void main(String[] args) {

        for (int i = 6; i >=0 ; i--) {


            for (int j = 0; j <=6 ; j++) {

                System.out.print(j +" ");

            }
            System.out.println();
        }


    }

}
